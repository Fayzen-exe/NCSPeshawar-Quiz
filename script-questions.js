/**
 * Match a topic string to best question bank(s)
 */
function NCS_matchTopic(topic){
  const t = topic.toLowerCase().trim();
  const scores = {};
  
  for(const [bankKey, bank] of Object.entries(NCS_QB)){
    let score = 0;
    // Check keywords
    for(const kw of bank.keywords){
      if(t.includes(kw)) score += kw.length > 4 ? 10 : 5;
      if(kw.includes(t) && t.length > 3) score += 8;
    }
    // Direct match
    if(t === bankKey) score += 20;
    if(bankKey.includes(t)) score += 10;
    if(scores[bankKey] === undefined) scores[bankKey] = 0;
    scores[bankKey] = score;
  }
  
  // Sort by score descending
  const sorted = Object.entries(scores).sort((a,b)=>b[1]-a[1]);
  
  // Return top matches with score > 0, or all banks if no match
  const matched = sorted.filter(([,s])=>s>0).map(([k])=>k);
  return matched.length ? matched : Object.keys(NCS_QB);
}

/**
 * Shuffle array using Fisher-Yates
 */
function NCS_shuffle(arr){
  const a = [...arr];
  for(let i=a.length-1;i>0;i--){
    const j=Math.floor(Math.random()*(i+1));
    [a[i],a[j]]=[a[j],a[i]];
  }
  return a;
}

/**
 * Get unique questions - tracks used questions per session
 */
const NCS_usedIds = new Set();

function NCS_resetUsed(){ NCS_usedIds.clear(); }

/**
 * Main generator: get questions by topic and type
 * @param {string} topic - User's topic
 * @param {string} qtype - 'mcq' | 'truefalse' | 'fill'
 * @param {number} count - Number of questions
 * @returns {Array} Array of question objects
 */
function NCS_getQuestions(topic, qtype, count){
  const matchedBanks = NCS_matchTopic(topic);
  const typeKey = qtype === 'truefalse' ? 'truefalse' : qtype === 'fill' ? 'fill' : 'mcq';
  
  // Collect all questions from matched banks
  let pool = [];
  for(const bankKey of matchedBanks){
    const bank = NCS_QB[bankKey];
    if(!bank[typeKey]) continue;
    bank[typeKey].forEach((q, idx)=>{
      const id = `${bankKey}_${typeKey}_${idx}`;
      if(!NCS_usedIds.has(id)){
        pool.push({...q, _id: id, type: typeKey === 'truefalse' ? 'truefalse' : typeKey === 'fill' ? 'fill' : 'mcq'});
      }
    });
  }
  
  // If pool too small, also pull from unmatched banks
  if(pool.length < count){
    for(const [bankKey, bank] of Object.entries(NCS_QB)){
      if(matchedBanks.includes(bankKey)) continue;
      if(!bank[typeKey]) continue;
      bank[typeKey].forEach((q, idx)=>{
        const id = `${bankKey}_${typeKey}_${idx}`;
        if(!NCS_usedIds.has(id)){
          pool.push({...q, _id: id, type: typeKey === 'truefalse' ? 'truefalse' : typeKey === 'fill' ? 'fill' : 'mcq'});
        }
      });
    }
  }
  
  // If still not enough, reset used and try again
  if(pool.length < count){
    NCS_resetUsed();
    pool = [];
    for(const bankKey of matchedBanks){
      const bank = NCS_QB[bankKey];
      if(!bank[typeKey]) continue;
      bank[typeKey].forEach((q, idx)=>{
        pool.push({...q, _id: `${bankKey}_${typeKey}_${idx}`, type: qtype === 'truefalse' ? 'truefalse' : qtype === 'fill' ? 'fill' : 'mcq'});
      });
    }
  }
  
  // Shuffle pool
  const shuffled = NCS_shuffle(pool);
  
  // Take required count
  const selected = shuffled.slice(0, count);
  
  // Mark as used
  selected.forEach(q => NCS_usedIds.add(q._id));
  
  // Clean up internal ID before returning
  return selected.map(({_id, ...q}) => {
    // For truefalse, ensure options are set
    if(q.type === 'truefalse' && !q.options){
      q.options = ['True','False'];
    }
    return q;
  });
}
